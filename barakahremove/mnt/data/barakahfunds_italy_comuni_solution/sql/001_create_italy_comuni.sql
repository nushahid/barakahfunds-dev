CREATE TABLE IF NOT EXISTS italy_comuni (
    id INT UNSIGNED NOT NULL AUTO_INCREMENT,
    istat_code VARCHAR(10) NOT NULL,
    comune_name VARCHAR(150) NOT NULL,
    comune_name_normalized VARCHAR(150) NOT NULL,
    province_code VARCHAR(10) DEFAULT NULL,
    province_name VARCHAR(100) DEFAULT NULL,
    region_code VARCHAR(10) DEFAULT NULL,
    region_name VARCHAR(100) DEFAULT NULL,
    cadastral_code VARCHAR(10) DEFAULT NULL,
    cap VARCHAR(20) DEFAULT NULL,
    is_active TINYINT(1) NOT NULL DEFAULT 1,
    source_name VARCHAR(100) DEFAULT NULL,
    source_updated_at DATE DEFAULT NULL,
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    PRIMARY KEY (id),
    UNIQUE KEY uniq_istat_code (istat_code),
    KEY idx_comune_name (comune_name),
    KEY idx_comune_name_normalized (comune_name_normalized),
    KEY idx_province_code (province_code),
    KEY idx_region_name (region_name),
    KEY idx_is_active (is_active)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
